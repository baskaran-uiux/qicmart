export type Language = keyof typeof translations
export type TranslationKey = keyof typeof translations['English']

export function getT(lang: string | null | undefined) {
    const selectedLang = (lang && (translations as any)[lang] ? lang : "English") as Language
    const tMap = translations[selectedLang] as any
    const engMap = translations["English"] as any
    return (key: TranslationKey): string => tMap[key] || engMap[key] || key
}
